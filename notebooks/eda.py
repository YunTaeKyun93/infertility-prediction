import pandas as pd

# 1. 데이터 불러오기
df = pd.read_csv('train.csv')

# 2. 합산할 불임 원인 컬럼 리스트 (10개)
cause_cols = [
    '불임 원인 - 난관 질환', '불임 원인 - 남성 요인', '불임 원인 - 배란 장애',
    '불임 원인 - 여성 요인', '불임 원인 - 자궁경부 문제', '불임 원인 - 자궁내막증',
    '불임 원인 - 정자 농도', '불임 원인 - 정자 면역학적 요인',
    '불임 원인 - 정자 운동성', '불임 원인 - 정자 형태'
]

# 3. 각 행(환자)별로 해당되는 불임 원인의 총 개수 계산
df['불임_원인_합계'] = df[cause_cols].sum(axis=1)

# 4. '임신 성공 여부'를 기준으로 합계의 평균 계산
# 성공(1) 그룹과 실패(0) 그룹의 평균적 원인 개수 비교
result = df.groupby('임신 성공 여부')['불임_원인_합계'].mean()

print("--- 임신 성공 여부별 불임 원인 합계의 평균 ---")
print(result)

# 5. 시각화로 확인 (선택 사항)
import seaborn as sns
import matplotlib.pyplot as plt

plt.figure(figsize=(8, 5))
sns.barplot(x=result.index, y=result.values)
plt.title('임신 성공 여부별 불임 원인 합계의 평균')
plt.ylabel('원인의 평균')
